import {httpApiUrl, wsApiUrl} from '../core/Api';


export function getList() {
    return fetch(`${httpApiUrl}/getRecFromServer`, {
        method: 'GET',
        headers: {
            'Accept': 'application/json',
            'Content-Type': 'application/json'
        }
    })
        .then((response) => response.json())
        .then((responseJson) => {
            return responseJson;
        })
        .catch((error) => {
            console.error(error);
        });
}
